def _weighted_choice(rng: random.Random, choices: list[tuple[Any, int, ...]]) -> tuple[Any, ...]:
    """Pick a row from a weighted-tuple list. Weight is the second element."""
    total = sum(c[1] for c in choices) if choices and len(choices[0]) >= 2 else 0
    pick = rng.uniform(0, total)
    upto = 0.0
    for c in choices:
        upto += c[1]
        if upto >= pick:
            return c
    return choices[-1]


def _pick_sr_mix(rng: random.Random, day_of_year: int) -> tuple[str, str, str]:
    """Pick (category, domain, priority).  Light seasonal bias."""
    weights = []
    for category, domain, base_w, prio in SR_MIX:
        w = base_w
        # Winter (day_of_year 0-90 / 335-365): more main-break complaints.
        if (day_of_year < 90 or day_of_year > 335) and category == "no_water":
            w *= 2
        # Summer (180-260): more discoloured-water during construction season.
        if 180 <= day_of_year <= 260 and category == "discoloured_water":
            w *= 1.7
        # Fall (260-335): more flooding (leaves clogging CBs).
        if 260 <= day_of_year <= 335 and category == "flooding":
            w *= 2.2
        weights.append((category, w, domain, prio))
    weighted = [(c[0], int(c[1] * 100), c[2], c[3]) for c in weights]
    pick = _weighted_choice(rng, [(c[0], c[1]) for c in weighted])
    chosen = next(c for c in weighted if c[0] == pick[0])
    return chosen[0], chosen[2], chosen[3]


def _pick_inspection_kind(rng: random.Random) -> str:
    pick = _weighted_choice(rng, INSPECTION_MIX)
    return pick[0]


def _random_business_dt(rng: random.Random, base_day: datetime) -> datetime:
    """Random datetime on `base_day` skewed toward business hours."""
    hour = rng.choices(
        population=list(range(24)),
        weights=[1, 1, 1, 1, 1, 2, 4, 6, 8, 10, 10, 9, 8, 8, 9, 8, 6, 4, 3, 2, 2, 2, 1, 1],
    )[0]
    minute = rng.randint(0, 59)
    return base_day.replace(hour=hour, minute=minute, second=rng.randint(0, 59), microsecond=0)


def _label_simulated(payload: dict[str, Any]) -> dict[str, Any]:
    return {**payload, "simulated": True}


def _maybe_pick_task(rng: random.Random, tasks_by_code: dict[str, TaskDefinition], category: str) -> TaskDefinition | None:
    code = SR_CATEGORY_TASK.get(category)
    if not code:
        return None
    return tasks_by_code.get(code)


def _next_sr_number(session, tenant_id: int, taken: set[str]) -> str:
    """Generate a unique SR number not colliding with existing or just-staged ones."""
    n = 100000
    while True:
        candidate = f"SR-{n}"
        if candidate not in taken:
            existing = session.scalar(
                select(ServiceRequest.id).where(
                    ServiceRequest.tenant_id == tenant_id,
                    ServiceRequest.sr_number == candidate,
                )
            )
            if existing is None:
                taken.add(candidate)
                return candidate
        n += 1


