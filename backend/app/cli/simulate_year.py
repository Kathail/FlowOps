"""``flask simulate-year`` — emit 12 months of synthetic operational data.

Picks up the demo tenant's existing baseline (users, crews, assets,
task definitions, schedules) and lays a realistic year of activity on
top: service requests with full intake → dispatch → close lifecycles,
work orders progressing through every status with task definitions,
task_data, time logs, and operator comments, inspections of every
kind, and entity links wiring the dispatched SRs to their WOs.

Idempotent: rows generated by this command are tagged with
``attrs.simulated = True``. Re-running with ``--force`` wipes those
rows before regenerating; without ``--force`` it refuses to run if any
simulated rows already exist for the tenant.

Deterministic: a single ``random.Random`` seed drives every random
choice so the same inputs reliably produce the same data.
"""

