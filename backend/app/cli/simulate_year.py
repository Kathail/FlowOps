# Wipe (only simulated rows)
# ---------------------------------------------------------------------------


def _wipe_simulated(tenant_id: int) -> None:
    """Delete only rows tagged ``attrs.simulated = True``."""
    # Time logs: cascade via WorkOrder when WO is deleted.
    sim_filter = lambda model: db.session.execute(
        model.__table__.delete().where(
            model.tenant_id == tenant_id,
            model.attrs["simulated"].astext.cast(db.Boolean) == True,  # noqa: E712
        )
    )

    # Comments: tagged via attrs? Comment lacks attrs — use a body marker.
    db.session.execute(
        Comment.__table__.delete().where(
            Comment.tenant_id == tenant_id,
            Comment.body.like("[sim]%"),
        )
    )
    # Entity links: tagged via note marker.
    db.session.execute(
        EntityLink.__table__.delete().where(
            EntityLink.tenant_id == tenant_id,
            EntityLink.note.like("[sim]%"),
        )
    )

    # Time logs cascade via FK ondelete=CASCADE on WorkOrder.id.
    db.session.execute(
        WorkOrderTimeLog.__table__.delete().where(
            WorkOrderTimeLog.work_order_id.in_(
                select(WorkOrder.id).where(
                    WorkOrder.tenant_id == tenant_id,
                    WorkOrder.attrs["simulated"].astext.cast(db.Boolean) == True,  # noqa: E712
                )
            )
        )
    )

    sim_filter(Inspection)
    sim_filter(WorkOrder)
    sim_filter(ServiceRequest)
    db.session.commit()


