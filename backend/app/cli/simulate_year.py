def _make_wo_standalone(
    *, rng, tenant, asset, domain, operators, crews, tasks_by_code, wo_taken, today, when,
):
    wo_type = rng.choices(["planned", "reactive"], weights=[6, 4])[0]
    if wo_type == "planned":
        # Planned work usually has a task definition (e.g. valve exercising).
        choices = [
            ("WAT-TASK-VALVE-EXERCISE", "valve_exercise"),
            ("WAT-TASK-HYDRANT-FLUSH", "flushing"),
            ("WAT-TASK-HYDRANT-FLOW", "inspection"),
            ("SEW-TASK-LIFT-STATION", "inspection"),
            ("SEW-TASK-MANHOLE-INSPECT", "inspection"),
            ("STM-TASK-CB-INSPECT", "inspection"),
        ]
        code, category = rng.choice(choices)
        task = tasks_by_code.get(code)
    else:
        # Reactive but not from an SR — eg supervisor created it.
        code = rng.choice([
            "WAT-TASK-VALVE-EXERCISE",
            "WAT-TASK-MAIN-BREAK",
            "SEW-TASK-BACKUP",
            "SEW-TASK-SLOW-DRAIN",
            "STM-TASK-CB-CLOGGED",
            None,
        ])
        task = tasks_by_code.get(code) if code else None
        category = (task.default_category if task and task.default_category else "repair")

    title = (task.title if task else "Field work") + (
        f" — {asset.asset_uid}" if asset else ""
    )
    return _make_wo(
        rng=rng,
        tenant=tenant,
        wo_taken=wo_taken,
        wo_number=_next_wo_number(db.session, tenant.id, wo_taken),
        title=title,
        category=category,
        priority=rng.choices(["low", "normal", "high"], weights=[2, 7, 2])[0],
        domain=domain,
        asset=asset,
        task=task,
        operators=operators,
        crews=crews,
        scheduled_for=when,
        today=today,
        wo_type=wo_type,
    )


def _make_wo(
    *, rng, tenant, wo_taken, wo_number, title, category, priority, domain,
    asset, task, operators, crews, scheduled_for, today, wo_type,
    service_request_id=None,
):
    operator = rng.choice(operators)
    crew = rng.choice(crews) if crews and rng.random() < 0.5 else None
    # Decide a final status with realistic mix.
    final = rng.choices(
        ["completed", "completed", "completed", "completed", "completed",
         "in_progress", "open", "assigned", "on_hold", "cancelled", "draft"],
        k=1,
    )[0]

    started_at = None
    completed_at = None
    if final in {"in_progress", "completed", "on_hold", "cancelled"}:
        started_at = scheduled_for + timedelta(hours=rng.randint(1, 18))
    if final == "completed":
        completed_at = (started_at or scheduled_for) + timedelta(
            hours=rng.randint(1, 12)
        )
        if completed_at > today:
            completed_at = today - timedelta(hours=1)

    task_data: dict[str, Any] = {}
    if task and task.code in TASK_DATA_PRESETS and final == "completed":
        task_data = dict(rng.choice(TASK_DATA_PRESETS[task.code]))

    wo = WorkOrder(
        tenant_id=tenant.id,
        wo_number=wo_number,
        type=wo_type,
        category=category,
        priority=priority,
        status=final,
        title=title,
        description=f"[sim] {title}",
        asset_id=asset.id if asset else None,
        # See SR comment above — location is POINT-only; rely on asset_id.
        service_request_id=service_request_id,
        scheduled_for=scheduled_for,
        started_at=started_at,
        completed_at=completed_at,
        assigned_to=operator.id if final not in {"draft", "cancelled"} else None,
        crew_id=crew.id if crew else None,
        task_definition_id=task.id if task else None,
        task_data=task_data,
        attrs=_label_simulated({"domain": domain}),
    )
    db.session.add(wo)
    db.session.flush()

    # Time logs for in-progress / completed WOs.
    if final in {"in_progress", "completed"} and started_at:
        n_logs = rng.choices([1, 2, 3], weights=[5, 4, 2])[0]
        cursor = started_at
        end_cap = completed_at or (started_at + timedelta(hours=4))
        for j in range(n_logs):
            seg_hours = rng.choice([0.5, 0.75, 1.0, 1.5, 2.0])
            seg_end = min(cursor + timedelta(hours=seg_hours), end_cap)
            if seg_end <= cursor:
                break
            db.session.add(WorkOrderTimeLog(
                work_order_id=wo.id,
                user_id=operator.id,
                started_at=cursor,
                ended_at=seg_end,
                hours_decimal=Decimal(
                    f"{(seg_end - cursor).total_seconds() / 3600:.2f}"
                ),
                notes=f"[sim] {rng.choice(['On site', 'Travel + setup', 'Repair work', 'Cleanup'])}",
            ))
            cursor = seg_end + timedelta(minutes=rng.randint(0, 30))
            if cursor >= end_cap:
                break

    # Operator comments on completed WOs — exercise the comment timeline
    # without depending on the smart-comment chip UI (the operator could
    # have typed any of these).
    wo._sim_comments_added = 0  # type: ignore[attr-defined]
    if final == "completed":
        comment_count = rng.choices([0, 1, 2], weights=[2, 5, 3])[0]
        for j in range(comment_count):
            body = rng.choice([
                "[sim] Arrived on site. Customer briefed.",
                "[sim] Work complete. Customer satisfied.",
                "[sim] No further action required.",
                "[sim] Notified supervisor of finding.",
            ])
            db.session.add(Comment(
                tenant_id=tenant.id,
                entity_type="work_order",
                entity_id=wo.id,
                body=body,
                created_by=operator.id,
                created_at=(started_at or scheduled_for)
                    + timedelta(minutes=rng.randint(15, 240)),
            ))
            wo._sim_comments_added += 1  # type: ignore[attr-defined]

    return wo


