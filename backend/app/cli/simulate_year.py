"""``flask simulate-year`` — emit 12 months of synthetic operational data.

Domain model the simulator targets:

- A **work order** is an investigator's daily assignment for one
  *system* (water / sewer / storm) — eg "Hydrant flushing — north
  district, Tuesday". The WO carries the day's *activity* task
  (``WAT-TASK-HYDRANT-FLUSH``, ``WAT-TASK-VALVE-EXERCISE``,
  ``SEW-TASK-LIFT-STATION``, etc.) and accumulates ``task_data`` as the
  operator works through the day.
- A **service request** is a citizen-reported issue (discoloured water,
  sewer backup, low pressure, …). It carries the *citizen-issue* task
  (``WAT-TASK-DISCOLOURED``, ``SEW-TASK-BACKUP``, ``WAT-TASK-LOW-PRESSURE``)
  with its own form / chips / completion contract.
- **SRs roll up under the day's WO for their system.** When a citizen
  reports something, the SR gets attached to whichever daily WO of the
  matching domain is closest to its reported_at date, via
  ``service_request.work_order_id`` (existing FK).

Idempotent: rows generated by this command are tagged with
``attrs.simulated = True``. Re-running with ``--force`` wipes those
rows before regenerating. Deterministic: a single ``random.Random``
seed drives every random choice.
"""

