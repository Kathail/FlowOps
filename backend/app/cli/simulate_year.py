# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _weighted_choice(rng: random.Random, choices: list[tuple[Any, int]]) -> Any:
    total = sum(w for _, w in choices)
    pick = rng.uniform(0, total)
    upto = 0.0
    for value, w in choices:
        upto += w
        if upto >= pick:
            return value
    return choices[-1][0]


def _label_simulated(payload: dict[str, Any]) -> dict[str, Any]:
    return {**payload, "simulated": True}


def _next_number(session, model, column, taken: set[str], prefix: str, start: int) -> str:
    n = start
    while True:
        candidate = f"{prefix}{n}"
        if candidate not in taken:
            existing = session.scalar(select(column).where(column == candidate))
            if existing is None:
                taken.add(candidate)
                return candidate
        n += 1


def _is_workday(d: date) -> bool:
    return d.weekday() < 5  # Mon-Fri


def _random_business_dt(rng: random.Random, base_day: date) -> datetime:
    hour = rng.choices(
        population=list(range(7, 18)),
        weights=[3, 6, 9, 10, 10, 8, 8, 9, 7, 5, 3],
    )[0]
    return datetime.combine(
        base_day,
        time(hour=hour, minute=rng.randint(0, 59), second=rng.randint(0, 59)),
        tzinfo=UTC,
    )


# ---------------------------------------------------------------------------
# Wipe (only simulated rows)
# ---------------------------------------------------------------------------


def _wipe_simulated(tenant_id: int) -> None:
    db.session.execute(
        Comment.__table__.delete().where(
            Comment.tenant_id == tenant_id,
            Comment.body.like("[sim]%"),
        )
    )
    db.session.execute(
        EntityLink.__table__.delete().where(
            EntityLink.tenant_id == tenant_id,
            EntityLink.note.like("[sim]%"),
        )
    )
    db.session.execute(
        WorkOrderTimeLog.__table__.delete().where(
            WorkOrderTimeLog.work_order_id.in_(
                select(WorkOrder.id).where(
                    WorkOrder.tenant_id == tenant_id,
                    WorkOrder.attrs["simulated"].astext.cast(db.Boolean) == True,  # noqa: E712
                )
            )
        )
    )
    # Detach simulated SRs from any WO before deleting WOs (FK is SET NULL,
    # but doing this explicitly keeps the order obvious).
    db.session.execute(
        Inspection.__table__.delete().where(
            Inspection.tenant_id == tenant_id,
            Inspection.attrs["simulated"].astext.cast(db.Boolean) == True,  # noqa: E712
        )
    )
    db.session.execute(
        ServiceRequest.__table__.delete().where(
            ServiceRequest.tenant_id == tenant_id,
            ServiceRequest.attrs["simulated"].astext.cast(db.Boolean) == True,  # noqa: E712
        )
    )
    db.session.execute(
        WorkOrder.__table__.delete().where(
            WorkOrder.tenant_id == tenant_id,
            WorkOrder.attrs["simulated"].astext.cast(db.Boolean) == True,  # noqa: E712
        )
    )
    db.session.commit()


# ---------------------------------------------------------------------------
# Generation
# ---------------------------------------------------------------------------


def _simulate(tenant: Tenant, *, seed: int, today: datetime) -> dict[str, int]:
    rng = random.Random(seed)
    counts = {"daily_wo": 0, "sr": 0, "sr_attached": 0, "inspection": 0,
              "comment": 0, "time_log": 0}

    users = db.session.execute(
        select(User).where(User.tenant_id == tenant.id)
    ).scalars().all()
    if not users:
        raise click.UsageError("Tenant has no users — seed-demo first")
    operators = [
        u for u in users
        if "tech" in (u.email or "") or "supervisor" in (u.email or "")
    ]
    if not operators:
        operators = users
    intake = next((u for u in users if "intake" in (u.email or "")), users[0])

    crews = db.session.execute(
        select(Crew).where(Crew.tenant_id == tenant.id)
    ).scalars().all()

    assets = db.session.execute(
        select(Asset).where(Asset.tenant_id == tenant.id)
    ).scalars().all()
    assets_by_class: dict[str, list[Asset]] = {}
    for a in assets:
        assets_by_class.setdefault(a.class_code, []).append(a)

    tasks = db.session.execute(
        select(TaskDefinition).where(
            TaskDefinition.tenant_id == tenant.id,
            TaskDefinition.status == "active",
        )
    ).scalars().all()
    tasks_by_code = {t.code: t for t in tasks}

    sr_taken: set[str] = set()
    wo_taken: set[str] = set()
    insp_taken: set[str] = set()

    domain_assets = {
        "water": assets_by_class.get("WAT_HYD", [])
                 + assets_by_class.get("WAT_MAIN", [])
                 + assets_by_class.get("WAT_VLV", [])
                 + assets_by_class.get("WAT_SVC", []),
        "sewer": assets_by_class.get("SAN_MAIN", [])
                 + assets_by_class.get("SAN_MH", [])
                 + assets_by_class.get("SAN_LFT", []),
        "storm": assets_by_class.get("STM_CB", [])
                 + assets_by_class.get("STM_MAIN", []),
    }
    for k, v in list(domain_assets.items()):
        if not v:
            domain_assets[k] = assets

    start_day = (today - timedelta(days=365)).date()
    end_day = today.date()

    # ---- Daily WOs: one per (system, workday) per active operator chunk ----
    # Realistic-ish density: each system gets a WO ~3-5 weekdays/week.
    daily_wos: list[WorkOrder] = []
    daily_wos_by_domain_date: dict[tuple[str, date], list[WorkOrder]] = {}
    cursor = start_day
    while cursor <= end_day:
        if _is_workday(cursor):
            for domain in ("water", "sewer", "storm"):
                # Each system has ~70% chance of a daily WO on any weekday.
                if rng.random() > 0.70:
                    continue
                recipe = _weighted_choice(
                    rng,
                    [(r, r[3]) for r in DAILY_BY_DOMAIN[domain]],
                )
                task_code, category, title_tpl, _w = recipe
                task = tasks_by_code.get(task_code)
                district = rng.choice(DISTRICTS)
                operator = rng.choice(operators)
                crew = rng.choice(crews) if crews and rng.random() < 0.4 else None
                scheduled = _random_business_dt(rng, cursor)

                # Status: most are completed (in the past), today's are
                # in_progress / open, future days are open.
                if cursor < end_day - timedelta(days=2):
                    final = rng.choices(
                        ["completed", "completed", "completed", "completed",
                         "cancelled"],
                        k=1,
                    )[0]
                elif cursor < end_day:
                    final = rng.choices(
                        ["completed", "in_progress", "open"], weights=[5, 3, 2],
                    )[0]
                else:
                    final = rng.choices(
                        ["assigned", "open", "in_progress"], weights=[4, 3, 2],
                    )[0]

                started_at = None
                completed_at = None
                if final in {"in_progress", "completed", "cancelled"}:
                    started_at = scheduled + timedelta(hours=rng.randint(0, 2))
                if final == "completed":
                    completed_at = started_at + timedelta(
                        hours=rng.randint(2, 7)
                    )
                    if completed_at > today:
                        completed_at = today - timedelta(hours=1)

                task_data: dict[str, Any] = {}
                if task and final == "completed" and task_code in WO_TASK_DATA_PRESETS:
                    task_data = dict(rng.choice(WO_TASK_DATA_PRESETS[task_code]))

                wo = WorkOrder(
                    tenant_id=tenant.id,
                    wo_number=_next_number(
                        db.session, WorkOrder, WorkOrder.wo_number,
                        wo_taken, "WO-", 200000,
                    ),
                    type="planned",
                    category=category,
                    priority="normal",
                    status=final,
                    title=title_tpl.format(district=district),
                    description=f"[sim] Daily {domain} crew assignment — {district} district.",
                    scheduled_for=scheduled,
                    started_at=started_at,
                    completed_at=completed_at,
                    assigned_to=operator.id if final not in {"draft"} else None,
                    crew_id=crew.id if crew else None,
                    task_definition_id=task.id if task else None,
                    task_data=task_data,
                    attrs=_label_simulated({
                        "domain": domain,
                        "district": district,
                        "daily": True,
                    }),
                )
                db.session.add(wo)
                db.session.flush()
                daily_wos.append(wo)
                daily_wos_by_domain_date.setdefault((domain, cursor), []).append(wo)
                counts["daily_wo"] += 1

                # Time logs on completed / in_progress daily WOs.
                if final in {"completed", "in_progress"} and started_at:
                    n_logs = rng.choices([2, 3, 4], weights=[3, 5, 2])[0]
                    log_cursor = started_at
                    end_cap = completed_at or (started_at + timedelta(hours=6))
                    for _ in range(n_logs):
                        seg = rng.choice([0.75, 1.0, 1.5, 2.0])
                        seg_end = min(log_cursor + timedelta(hours=seg), end_cap)
                        if seg_end <= log_cursor:
                            break
                        db.session.add(WorkOrderTimeLog(
                            work_order_id=wo.id,
                            user_id=operator.id,
                            started_at=log_cursor,
                            ended_at=seg_end,
                            hours_decimal=Decimal(
                                f"{(seg_end - log_cursor).total_seconds() / 3600:.2f}"
                            ),
                            notes=f"[sim] {rng.choice(['Travel', 'On site', 'Documenting'])}",
                        ))
                        counts["time_log"] += 1
                        log_cursor = seg_end + timedelta(minutes=rng.randint(0, 30))
                        if log_cursor >= end_cap:
                            break

                # Operator wrap-up comments on completed WOs.
                if final == "completed":
                    for _ in range(rng.choices([1, 2], weights=[6, 4])[0]):
                        db.session.add(Comment(
                            tenant_id=tenant.id,
                            entity_type="work_order",
                            entity_id=wo.id,
                            body=f"[sim] {rng.choice(['Route complete.', 'No issues found on route.', 'All assignments closed for the day.'])}",
                            created_by=operator.id,
                            created_at=(completed_at or scheduled),
                        ))
                        counts["comment"] += 1
        cursor += timedelta(days=1)

    db.session.flush()

    # ---- Citizen SRs ----
    target_srs = 200
    for i in range(target_srs):
        day_offset = rng.randint(0, 364)
        when = _random_business_dt(rng, start_day + timedelta(days=day_offset))
        category, domain, base_w, priority = rng.choice(SR_MIX)
        candidates = domain_assets.get(domain) or assets
        asset = rng.choice(candidates) if candidates else None
        sr_task_code = SR_CATEGORY_TASK.get(category)
        sr_task = tasks_by_code.get(sr_task_code) if sr_task_code else None

        # Decide lifecycle outcome.
        roll = rng.random()
        if roll < 0.65:
            sr_status = "dispatched"  # rolled into a daily WO
        elif roll < 0.78:
            sr_status = "closed"
        elif roll < 0.86:
            sr_status = "duplicate"
        elif roll < 0.93:
            sr_status = "triaged"
        else:
            sr_status = "new"

        # Populate task_data when the SR has been worked.
        sr_task_data: dict[str, Any] = {}
        if (sr_status in {"dispatched", "closed"}
                and sr_task_code
                and sr_task_code in SR_TASK_DATA_PRESETS):
            sr_task_data = dict(rng.choice(SR_TASK_DATA_PRESETS[sr_task_code]))

        sr = ServiceRequest(
            tenant_id=tenant.id,
            sr_number=_next_number(
                db.session, ServiceRequest, ServiceRequest.sr_number,
                sr_taken, "SR-", 100000,
            ),
            category=category,
            domain=domain,
            priority=priority,
            status=sr_status,
            reported_at=when,
            caller_name=rng.choice(CALLER_NAMES),
            caller_phone=f"555-{rng.randint(1000, 9999)}",
            reported_address=getattr(asset, "address_cached", None)
                or f"{rng.randint(10, 9999)} Main St",
            asset_id=asset.id if asset else None,
            description=rng.choice(
                SR_DESCRIPTIONS.get(category, ["Reported issue."])
            ),
            intake_user_id=intake.id,
            task_definition_id=sr_task.id if sr_task else None,
            task_data=sr_task_data,
            closed_at=(when + timedelta(hours=rng.randint(2, 48)))
                if sr_status in {"closed", "duplicate"} else None,
            closure_reason=("resolved" if sr_status == "closed"
                            else "duplicate" if sr_status == "duplicate"
                            else None),
            attrs=_label_simulated({}),
        )
        db.session.add(sr)
        db.session.flush()
        counts["sr"] += 1

        # Attach to the day's WO for this domain (or the closest workday's
        # WO) — only meaningful for SRs that were worked.
        if sr_status in {"dispatched", "closed"}:
            attached = _attach_sr_to_daily_wo(
                rng=rng,
                tenant_id=tenant.id,
                sr=sr,
                domain=domain,
                reported_on=when.date(),
                daily_wos_by_domain_date=daily_wos_by_domain_date,
                intake_user_id=intake.id,
            )
            if attached:
                counts["sr_attached"] += 1

        # Light comments on a fraction of SRs.
        for _ in range(rng.choices([0, 1, 2], weights=[55, 32, 13])[0]):
            db.session.add(Comment(
                tenant_id=tenant.id,
                entity_type="service_request",
                entity_id=sr.id,
                body=f"[sim] {rng.choice(['Customer called back to confirm details.', 'Spoke with neighbour — same issue reported.', 'Left voicemail with caller.'])}",
                created_by=intake.id,
                created_at=when + timedelta(hours=rng.randint(1, 48)),
            ))
            counts["comment"] += 1

        if i % 25 == 0:
            db.session.flush()

    # ---- Inspections (independent of WO/SR lifecycle) ----
    target_inspections = 280
    for i in range(target_inspections):
        day_offset = rng.randint(0, 364)
        when = _random_business_dt(rng, start_day + timedelta(days=day_offset))
        kind = _weighted_choice(rng, INSPECTION_MIX)
        hint = INSPECTION_CLASS_HINT[kind]
        candidates = assets_by_class.get(hint) or assets
        asset = rng.choice(candidates) if candidates else None
        operator = rng.choice(operators)
        condition = rng.randint(1, 5)
        ins = Inspection(
            tenant_id=tenant.id,
            inspection_number=_next_number(
                db.session, Inspection, Inspection.inspection_number,
                insp_taken, "INS-", 300000,
            ),
            kind=kind,
            asset_id=asset.id if asset else None,
            performed_at=when,
            performed_by=operator.id,
            overall_condition=condition,
            pass_=condition <= 3,
            notes=rng.choice([
                "[sim] Routine inspection. No issues.",
                "[sim] Sediment level above threshold — schedule cleaning.",
                "[sim] All measurements within tolerance.",
            ]),
            data={"simulated": True},
            attrs=_label_simulated({}),
        )
        db.session.add(ins)
        counts["inspection"] += 1
        if i % 50 == 0:
            db.session.flush()

    db.session.commit()
    return counts


def _attach_sr_to_daily_wo(
    *,
    rng: random.Random,
    tenant_id: int,
    sr: ServiceRequest,
    domain: str,
    reported_on: date,
    daily_wos_by_domain_date: dict[tuple[str, date], list[WorkOrder]],
    intake_user_id: int,
) -> bool:
    """Attach an SR to the day's WO for its system. If no WO exists on the
    exact day, walks forward up to 5 weekdays to find one."""
    candidate_dates = [reported_on + timedelta(days=offset) for offset in range(0, 6)]
    for candidate in candidate_dates:
        wos = daily_wos_by_domain_date.get((domain, candidate))
        if not wos:
            continue
        wo = rng.choice(wos)
        sr.work_order_id = wo.id
        db.session.add(EntityLink(
            tenant_id=tenant_id,
            source_type="service_request",
            source_id=sr.id,
            target_type="work_order",
            target_id=wo.id,
            kind="caused_by",
            note=f"[sim] rolled into daily {domain} WO",
            created_by=intake_user_id,
        ))
        return True
    return False


